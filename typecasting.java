
import java.io.*;
public class typecasting {
    
    public static void main(String args[])
    {
        byte age=29;
        int new_age=age;
        System.out.println(new_age);
       short age1=20000;
       int age2=age1;
        System.out.println(age2);
        float f=43.7f;
        int f1=(int)f;
        System.out.println(f1);
}}
